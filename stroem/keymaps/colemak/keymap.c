enum custom_keycodes {
    M_DIA = SAFE_RANGE,
    M_ESZ
};

// setxkbmap -option compose:ralt

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed) {
        switch(keycode) {
            case M_DIA:
                SEND_STRING(SS_RALT(SS_LSFT("'")));
                return false;
            case M_ESZ:
                SEND_STRING(SS_RALT("ss"));
                return false;
        }
    }
    return true;
};

