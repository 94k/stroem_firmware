const macro_t *action_get_macro(keyrecord_t *record, uint8_t id, uint8_t opt) {
    if (record->event.pressed) {
        switch(id) {
            case 0:
                return MACRO(D(RALT), D(LSFT), T(QUOTE) , U(LSFT), U(RALT), END);
            case 1:
                return MACRO(D(RALT), T(S), U(RALT), END);
        }
    }
    return MACRO_NONE;
};
